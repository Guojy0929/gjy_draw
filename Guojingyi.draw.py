import turtle as t
t.setup(800,800)
t.pensize(6)
t.color("red","yellow")
t.begin_fill()
t.circle(90)
t.end_fill()

t.penup()
t.goto(-40,110)
t.pendown()
t.pensize(3)
t.color("blue","black")
t.begin_fill()
t.circle(14)
t.end_fill()

t.penup()
t.goto(40,110)
t.pendown()
t.pensize(3)
t.color("blue","black")
t.begin_fill()
t.circle(14)
t.end_fill()

t.penup()
t.goto(55,70)
t.pendown()
t.pensize(3)
t.color("red")
t.begin_fill()
t.circle(10)
t.end_fill()

t.penup()
t.goto(-55,70)
t.pendown()
t.pensize(3)
t.color("red")
t.begin_fill()
t.circle(10)
t.end_fill()

t.penup()
t.goto(-40,80)
t.pendown()
t.right(90)
t.pensize(3)
t.color("red")
t.circle(45,180)
t.hideturtle
t.done
